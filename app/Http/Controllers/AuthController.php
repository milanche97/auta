    /**
     * Get the token array structure.
     *
     * @param  string $token
     *
     * @return array
     */
    protected function responeWithToken($token): array
    {
        return [
            'access_token' => $token,
            'token_type' => 'bearer',
            'expires_in' => auth()->factory()->getTTL() * 60
        ];
    }
}
